#!/usr/bin/env python3

def adder(arg1: int, arg2: int) -> int:
    """
    Function that sums two integers

    Args:
        arg1 (int): first number to be added
        arg2 (int): second number to be added

    Returns:
        int: Sum of arg1 + arg2

    Examples:
        >>> adder(3, 4)
        7

        >>> adder(2, 2)
        4

    """

    return int(arg1 + arg2)

